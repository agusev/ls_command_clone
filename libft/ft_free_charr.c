#include "libft.h"

void	ft_free_charr(char **a, int count)
{
	int		i;

	i = 0;
	while ((i != 0 && i < count) || (i == 0 && a[i] != NULL))
	{
		ft_strdel(&(a[i]));
		i += 1;
	}
	ft_memdel((void **)a);
}
